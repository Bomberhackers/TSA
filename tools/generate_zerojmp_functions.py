prefix = "asmus"

def process_file(i, filename):
    f = open(prefix + "/" + filename) # open file
    lines = f.readlines()
    base_folder = filename.split('/')[1]
    base_filename = filename.split('/')[2]

    addr_line = lines[13]
    pre_address = addr_line.split()[2]
    address = pre_address.upper()
    num_address = hex(int(address, base=16))

    # print the zerojmp function names
    for index in range(0, 325):
        print(evfunc_list[index] + "_" + num_address.split("x")[1] + "_" + base_folder + "_" +  base_filename + " = " + num_address + "; // segment:ovl_" + base_folder + "_" + base_filename)
        num_address = hex(int(num_address, base=16) + 12)

    return

id_num = 0
for i in file_list:
    process_file(id_num, i)
    id_num = id_num + 1
